public class Cuboid {
    public static void main(String[] args) {

        double lenght = 12.4;
        double width = 20.1;
        double height = 15.7;

        double volume = lenght + width + height;
        double surface = lenght * width * height;

        System.out.println("Volume: " + volume);
        System.out.println("Surface: " + surface);




    }

}

